# Variables.
a=5
b=13
c=12

# Order of Operations.
result1=a+b*c
print("a + b * c = ",result1)

# With parentheses: addition first.
result2=(a+b)*c
print("(a + b) * c = ",result2)

# More variations.
result3= a*(b+c)
print("a *(b * c) = ",result3)

result4= (a+b+c)
print("(a + b + c) = ",result4)